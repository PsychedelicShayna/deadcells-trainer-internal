int32_t InertiaMultiPatch::Patch() {
    int32_t return_values { 0 };

    byteptr_t search_start_location { nullptr };

    std::vector<uintptr_t> match_locations;

    for(ght::internal::GenericAssemblyPatch& patch : InertiaPatches) {
        if(patch.LatestMatchLocation != nullptr) patch.SearchStartLocation = patch.LatestMatchLocation;
        else patch.SearchStartLocation = search_start_location;

        return_values += patch.Patch();
        byteptr_t match_location { patch.LatestMatchLocation };

        match_locations.emplace_back(reinterpret_cast<uintptr_t>(match_location));

        if(match_location != nullptr) {
            if(reinterpret_cast<uintptr_t>(match_location) >= 0x007B0000) {
                search_start_location = match_location - 0x007B0000;
            } else {
                search_start_location = nullptr;
            }
        }
    }

    std::cout << "\n[";
    for(size_t i { 0 }; i < match_locations.size(); ++i) {
        if(i) std::cout << ", ";
        const uintptr_t& loc { match_locations[i] };
        std::cout << "0x" << std::setw(2) << std::setfill('0') << std::hex << loc + InertiaPatches[i].OffsetFromPatternStart;
    }
    std::cout << "]\n";

    isPatched = true;

    return return_values;
}

int32_t InertiaMultiPatch::Restore() {
    int32_t return_values { 0 };

    for(ght::internal::GenericAssemblyPatch& patch : InertiaPatches) {
        patch.SearchStartLocation = patch.LatestMatchLocation - 1024;
        return_values += patch.Restore();
    }

    isPatched = false;
    return return_values;
}

int32_t InertiaMultiPatch::Toggle() {
    return IsPatched() ? Restore() : Patch();
}

